//## La soustraction ##//
//la soustraction de nb1 et de nb2 avec l'operateur "-"
resultat = nb1 - nb2;
console.log("la soustraction de nb1 et nb2 = "- resultat);

//## La multiplication ##//
//la multiplication de nb1 et nb2 avec l'operateur "*"
console.log("la multiplication de nb1 et nb2 =" * resultat);

//## La division ##//
// la division de nb1 et nb2 avec l'operateur "/"
resultat = nb1 / nb2;
console.log("la division de nb1 et nb2 =" / resultat);

//## Le modulo ##//
// le modulo de nb1 et nb2 avec l'operateur "%"
nb1 = 11;
resultat = nb1 % nb2;
console.log("le modulo de nb1 et nb2 = " + resultat);

